     subroutine egrad_h2co(cood,natoms,e_evb,pot_grad,info)
       use h2co_mod
       implicit none
       integer::i,j,k,l,info,natoms
       real(kind=8)::f,V,getpot,step
       real(kind=8)::cood(3,4),com(3)
       real(kind=8)::y(6),r(6)
       real(kind=8)::bas(coff)
       real(kind=8)::e_upper,e_lower,e_evb
       real(kind=8)::pot_grad(3,4)
   real*8 smallCH, smallOH
   real*8 S, x,z,x3,x2,z1,z2
!	This probram expects cood to be in the order C,O,H,H
!   write(6,*) 'C ', (cood(i,1) ,i=1,3)
!   write(6,*) 'O ', (cood(i,2) ,i=1,3)
!   write(6,*) 'H ', (cood(i,3) ,i=1,3)
!   write(6,*) 'H ', (cood(i,4) ,i=1,3)


   call edis(r(1),y(1),cood(:,3),cood(:,4))
   call edis(r(2),y(2),cood(:,2),cood(:,3))
   call edis(r(3),y(3),cood(:,1),cood(:,3))
   call edis(r(4),y(4),cood(:,1),cood(:,4))
   call edis(r(5),y(5),cood(:,2),cood(:,4))
   call edis(r(6),y(6),cood(:,1),cood(:,2))

   call basis_h2co(bas,y)

    if (r(3)< r(4)) then
       smallCH=r(3)
       smallOH=r(2)
   else
       smallCH=r(4)
       smallOH=r(5)
    end if
! Switching function has been used here |
       x=r(1)
       z=x-8
       z1=10-8
       z2=z/z1
      x2=z2*z2
      x3=x2*z2

     if (x.ge. 10 ) then
         s=1.0
     else if (x.le. 8 ) then
         s=0.0d0
     else
         s=10*x3-15*x3*z2+6*x3*x2
     end if

     f=0
     do i=1,coff
        f=f+cof(i)*bas(i)
     end do
     f=f+114.332958863-1.059892782251382E-004
    if (x .ge. 8) then
       call hcopot(V,smallCH,r(6),smallOH)
       f= f*(1-s)+V*s
     end if
!..................................................................|

    getpot=f
    e_evb=getpot
!
!    Calculate the numerical gradient!
!
    step=0.001d0
    do i=1,4
       do j=1,3
          do k=1,2
             if (k.eq.1) then
                cood(j,i)=cood(j,i)+step
             else if (k.eq.2) then
                cood(j,i)=cood(j,i)-2d0*step
             end if

             call edis(r(1),y(1),cood(:,3),cood(:,4))
             call edis(r(2),y(2),cood(:,2),cood(:,3))
             call edis(r(3),y(3),cood(:,1),cood(:,3))
             call edis(r(4),y(4),cood(:,1),cood(:,4))
             call edis(r(5),y(5),cood(:,2),cood(:,4))
             call edis(r(6),y(6),cood(:,1),cood(:,2))


             call basis_h2co(bas,y)

             if (r(3)< r(4)) then
                smallCH=r(3)
                smallOH=r(2)
             else
                smallCH=r(4)
                smallOH=r(5)
             end if

! Switching function has been used here |
             x=r(1)
             z=x-8
             z1=10-8
             z2=z/z1
             x2=z2*z2
             x3=x2*z2

             if (x.ge. 10 ) then
                s=1.0
             else if (x.le. 8 ) then
                s=0.0d0
             else
                s=10*x3-15*x3*z2+6*x3*x2
             end if

             f=0
             do l=1,coff
                f=f+cof(l)*bas(l)
             end do
             f=f+114.332958863-1.059892782251382E-004

             if (x .ge. 8) then
                call hcopot(V,smallCH,r(6),smallOH)
                f= f*(1-s)+V*s
             end if
!..................................................................|

             getpot=f

             if (k.eq.1) then
                e_upper=getpot
             else if (k.eq.2) then
                e_lower=getpot
             end if
          end do
          cood(j,i)=cood(j,i)+step
          pot_grad(j,i)=(e_upper-e_lower)/(2d0*step)
       end do
    end do



   return
 end subroutine egrad_h2co

 subroutine edis(dis,edist,atomi,atomj)
   use h2co_mod
   real(kind=8)::atomi(3),atomj(3)
   real(kind=8)::dis,alfa
   real(kind=8),intent(out)::edist
   alfa=2.0d0
   dis=sqrt((atomi(1)-atomj(1))**2+(atomi(2)-atomj(2))**2+(atomi(3)-atomj(3))**2)
   edist=exp(-dis/alfa)

   return
 end subroutine edis

 subroutine basis_h2co(bas,y)
   use h2co_mod
   integer m,n,p,q,i,j,k
   real(kind=8)::bas(coff)
   real(kind=8)::y(6)
   do k=1,coff
     bas(k)=(y(1)**power(k,1))*(y(6)**power(k,6))*((y(2)**power(k,2))* &
            & (y(3)**power(k,3))*(y(4)**power(k,4))*(y(5)**power(k,5))+ &
            & (y(2)**power(k,5))*(y(3)**power(k,4))*(y(4)**power(k,3))* &
            & (y(5)**power(k,2)))
   end do
   return
 end subroutine



